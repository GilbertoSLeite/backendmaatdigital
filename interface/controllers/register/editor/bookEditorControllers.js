const getPostPutBookEditor = require('../../../../applications/businessRules/register/editor/businessRulesBookEditor');

const PostBookEditor = async (request, response) => {
  try {
    return await getPostPutBookEditor.BookEditorPost(request, response);
  } catch (error) {
    // eslint-disable-next-line no-console
    console.log('Error: ', error);
    response.status(404).send(JSON.stringify({
      status: Boolean(false),
      errorFull: error,
      errorCode: response.statusCode,
      errorMessage: response.statusMessage,
      errorDetail: error.parent,
    }));
    return error;
  }
};

